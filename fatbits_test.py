def test_bitmap_2():
    bits = '''
    01110
    10001
    00001
    00110
    01000
    10000
    11111
    '''
    expected = bytes(int(s, 2) for s in bits.strip().split('\n'))
    print(expected)
    assert expected == bitmap('2')


